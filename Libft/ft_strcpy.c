#include <string.h>
#include "libft.h"

char	*ft_strcpy(char *dest, const char *src)
{
	int		i;

	i = 0;
	while (src[i])
	{
		dest[i] = (char)src[i];
		i++;
	}
	dest[i] = (char)src[i];
	return (dest);
}
