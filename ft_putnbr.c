#include "libft.h"

void	ft_putnbr(int n)
{
	long j;

	j = n;
	if (j < 0)
	{
		ft_putchar('-');
		j = j * -1;
	}
	if (j >= 10)
	{
		ft_putnbr((j / 10));
	}
	ft_putchar((j % 10) + '0');
}
