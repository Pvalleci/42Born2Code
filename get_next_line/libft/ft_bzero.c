#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	char	*str;
	int		j;
	int		i;

	i = 0;
	str = (char*)s;
	j = (int)n;
	while (i < j)
	{
		str[i] = '\0';
		i++;
	}
}
