#include "libft.h"

int		ft_sqrt(int nb)
{
	int a;
	int b;

	a = nb;
	b = 1;
	while (a > b)
	{
		a = (a + b) / 2;
		b = nb / a;
	}
	if ((a * a) == nb)
		return (a);
	else
		return (0);
}
